"use client"

import React, { useEffect } from 'react'
import { useSimpleAuth } from './simple-auth-context'
import { Loader2 } from 'lucide-react'
import { useRouter } from 'next/navigation'

interface SimpleProtectedRouteProps {
  children: React.ReactNode
  allowedRoles?: string[]
  fallback?: React.ReactNode
  redirectTo?: string
}

export function SimpleProtectedRoute({ 
  children, 
  allowedRoles,
  fallback,
  redirectTo = '/auth/login'
}: SimpleProtectedRouteProps) {
  const { user, profile, loading } = useSimpleAuth()
  const router = useRouter()

  // Debug logging
  console.log('SimpleProtectedRoute Debug:', { 
    loading, 
    user: user ? 'exists' : 'null', 
    profile: profile ? 'exists' : 'null',
    userRole: profile?.role,
    allowedRoles 
  });

  // Handle redirect in useEffect to avoid setState during render
  useEffect(() => {
    if (!loading && (!user || !profile) && !fallback) {
      router.push(redirectTo)
    }
  }, [loading, user, profile, fallback, router, redirectTo])

  // Show loading state
  if (loading) {
    return (
      <div className="min-h-screen flex items-center justify-center bg-gray-50">
        <div className="text-center">
          <Loader2 className="h-8 w-8 animate-spin mx-auto mb-4 text-blue-600" />
          <p className="text-gray-600">Loading authentication...</p>
          <div className="mt-4 p-4 bg-blue-50 border border-blue-200 rounded">
            <p className="text-sm text-blue-700">Checking user session...</p>
          </div>
        </div>
      </div>
    )
  }

  // Not authenticated
  if (!user || !profile) {
    if (fallback) {
      return <>{fallback}</>
    }
    
    // Show loading message while redirect happens
    return (
      <div className="min-h-screen flex items-center justify-center bg-gray-50">
        <div className="text-center">
          <Loader2 className="h-8 w-8 animate-spin mx-auto mb-4 text-blue-600" />
          <p className="text-gray-600">Redirecting to login...</p>
        </div>
      </div>
    )
  }

  // Check role permissions
  if (allowedRoles && !allowedRoles.includes(profile.role)) {
    return (
      <div className="min-h-screen flex items-center justify-center bg-gray-50">
        <div className="max-w-md w-full bg-white rounded-lg shadow-lg p-8 text-center">
          <div className="mb-4">
            <div className="mx-auto w-16 h-16 bg-red-100 rounded-full flex items-center justify-center">
              <svg className="w-8 h-8 text-red-600" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M12 9v2m0 4h.01m-6.938 4h13.856c1.54 0 2.502-1.667 1.732-3L13.732 4c-.77-1.333-2.694-1.333-3.464 0L3.34 16c-.77 1.333.192 3 1.732 3z" />
              </svg>
            </div>
          </div>
          <h2 className="text-2xl font-bold text-gray-900 mb-2">Access Denied</h2>
          <p className="text-gray-600 mb-4">
            You don't have permission to access this page.
          </p>
          <div className="bg-gray-50 rounded-lg p-4 mb-4">
            <p className="text-sm text-gray-700">
              <span className="font-medium">Your role:</span> {profile.role.replace(/_/g, ' ')}
            </p>
            {allowedRoles && (
              <p className="text-sm text-gray-700 mt-2">
                <span className="font-medium">Required roles:</span> {allowedRoles.map(r => r.replace(/_/g, ' ')).join(', ')}
              </p>
            )}
          </div>
          <button
            onClick={() => router.push('/')}
            className="w-full bg-blue-600 text-white py-2 px-4 rounded-lg hover:bg-blue-700 transition-colors"
          >
            Go to Home
          </button>
        </div>
      </div>
    )
  }

  return <>{children}</>
}
